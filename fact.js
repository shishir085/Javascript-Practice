function fact(n) {
    let result =1
    while (n>1) {
        result=result*n
        n--;
    }
    return result
}
// console.log(fact(5))



function fact1(n) {
    
    let b=1
    for(i=1;i<=n;i++){
        b=b*i
    }
    return b
}

